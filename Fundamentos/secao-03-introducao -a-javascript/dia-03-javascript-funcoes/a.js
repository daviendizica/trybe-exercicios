

let saldo = 2000;

function somaSaldo(valor){
    return valor + saldo;
}

function subtraiSaldo(valor){
    return valor - saldo;
}

function multiplicaSaldo(valor){
    return valor * saldo;
}

function divideSaldo(valor){
    return valor / saldo;
}



console.log(somaSaldo(300));
console.log(subtraiSaldo(300));
console.log(multiplicaSaldo(300));
console.log(divideSaldo(300));

